// ==   : igualdad
// ===  : para comparar igualda en valor y en tipo de dato
// !=   : es diferente a, true: diferente o false: igual
// !==  : es diferente en valor y en tip[o de dato
 
// >    : mayor que
// <    : menor que
// >=   : mayor e iagula que
// <=   : menor e igual que
 
const a = 10;
const b = 20;
const c = "10"
 
console.log(a == b) //false
console.log(a === c) //false
console.log(a == c) //true
console.log(a != c) //false -> si se Interpreta como iguales
console.log(a !== c) // true
 
 
 
 
 
 